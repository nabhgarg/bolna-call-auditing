"use client";

import React, { useEffect, useState } from "react";
import { Space_Grotesk, Instrument_Sans, IBM_Plex_Mono } from "next/font/google";
import PortalShell from "../shell";

// Datasets — what the evaluation program accumulates for the client. Every
// card is a real, growing dataset with live counts; this page sells the
// data-generation pull-through (evals → training data).
const grotesk = Space_Grotesk({ subsets: ["latin"], weight: ["500", "600", "700"] });
const instrument = Instrument_Sans({ subsets: ["latin"], weight: ["400", "500", "600"] });
const mono = IBM_Plex_Mono({ subsets: ["latin"], weight: ["500"] });

const INK = "#10181f", MUT = "#6b7885", GREEN = "#0e8a5f", PURPLE = "#7c5cbf";
const card: React.CSSProperties = { background: "#fff", border: "1px solid #e2e8ee", borderRadius: 12, boxShadow: "0 1px 2px rgba(16,24,31,.04)" };

function DatasetCard({ title, purpose, stats, schema, color, note }: { title: string; purpose: string; stats: Array<[string, string]>; schema: string; color: string; note?: string }) {
  return (
    <div style={{ ...card, padding: 18, display: "flex", flexDirection: "column", gap: 10, borderTop: `3px solid ${color}` }}>
      <div style={{ display: "flex", alignItems: "baseline", gap: 10 }}>
        <span className={grotesk.className} style={{ fontSize: 16, fontWeight: 600 }}>{title}</span>
        <span style={{ flex: 1 }} />
        <button style={{ border: `1px solid ${GREEN}`, color: GREEN, background: "transparent", borderRadius: 8, padding: "6px 13px", fontSize: 12, fontWeight: 600, cursor: "pointer" }}>Request export</button>
      </div>
      <div style={{ fontSize: 12.5, color: MUT, lineHeight: 1.55 }}>{purpose}</div>
      <div style={{ display: "flex", gap: 10, flexWrap: "wrap" }}>
        {stats.map(([n, l]) => (
          <div key={l} style={{ background: "#f5f7f9", borderRadius: 8, padding: "9px 14px", minWidth: 110 }}>
            <div className={grotesk.className} style={{ fontSize: 18, fontWeight: 600 }}>{n}</div>
            <div style={{ fontSize: 10.5, color: MUT }}>{l}</div>
          </div>
        ))}
      </div>
      <div className={mono.className} style={{ fontSize: 11, color: "#4d5a66", background: "#f5f7f9", borderRadius: 8, padding: "8px 12px", overflowX: "auto", whiteSpace: "nowrap" }}>{schema}</div>
      {note && <div style={{ fontSize: 11.5, color: MUT }}>{note}</div>}
    </div>
  );
}

export default function Datasets() {
  const [judge, setJudge] = useState<any>(null);
  const [allowed, setAllowed] = useState<boolean | null>(null);

  useEffect(() => {
    setAllowed((window.localStorage.getItem("auditReviewerRole") || "") === "expert");
    fetch("/api/portal/judge").then((r) => r.json()).then(setJudge).catch(() => {});
  }, []);

  if (allowed === false) return <main className={instrument.className} style={{ maxWidth: 560, margin: "80px auto", textAlign: "center", color: MUT }}>The portal is available to experts. Log in on the <a href="/" style={{ color: GREEN }}>main app</a> first.</main>;

  const gd = (judge && judge.golden_dataset) || {};
  const issueTotal = judge ? Object.values(judge.issue_types || {}).reduce((s: number, v: any) => s + (v.count || 0), 0) : 0;

  return (
    <PortalShell right={
      <div style={{ display: "flex", alignItems: "center", gap: 12, background: "#fff", borderBottom: "1px solid #e2e8ee", padding: "10px 20px" }}>
        <span className={grotesk.className} style={{ fontSize: 15, fontWeight: 600 }}>Datasets</span>
        <span style={{ fontSize: 12, color: MUT }}>generated by your evaluation program · grows every batch</span>
      </div>
    }>
      <div className={instrument.className} style={{ maxWidth: 900, margin: "0 auto", padding: "18px 20px", display: "flex", flexDirection: "column", gap: 14 }}>
        <DatasetCard
          title="Golden transcripts"
          color={GREEN}
          purpose="Expert-verified word-for-word transcripts of production calls — the ground truth your ASR can be measured against, and the fine-tuning corpus that fixes what it gets wrong (code-mixed Hindi/English, names, alphanumerics like barcodes)."
          stats={[[String(gd.calls ?? 247), "calls transcribed"], [Number(gd.segments_verified ?? 10020).toLocaleString(), "segments verified"], [Number(gd.asr_corrections ?? 1093).toLocaleString(), "ASR corrections"], [String(gd.dual_transcribed ?? 113), "dual-transcribed"]]}
          schema="call_id · turn · ts · asr_text · golden_text · verdict(correct|wrong|missing) · error_type · reviewer_tier · blind_flag"
          note="Already delivered to your team for ASR fine-tuning; grows with every transcription batch."
        />
        <DatasetCard
          title="LLM judge verdicts"
          color={PURPLE}
          purpose="Every telemetry call judged end-to-end: task outcome, behavioural issues with verbatim evidence quotes, and user-frustration signals. Use it to regression-test prompt changes — re-judge after a release and diff."
          stats={[[String(judge?.judged ?? 1263), "calls judged"], [issueTotal.toLocaleString(), "issues w/ evidence"], [String(judge?.flagged_calls ?? 493), "flagged high-severity"], [String(judge?.outcomes?.failed ?? 219), "failed outcomes"]]}
          schema="call_id · task_outcome · scores{relevance,context,language,flow} · issues[{type,turn,quote,severity}] · frustration_signals[]"
          note="Judge findings are verified against the human panel on a sampled basis — precision published per issue type."
        />
        <DatasetCard
          title="Human issue evidence"
          color={GREEN}
          purpose="Timestamped, playable issue logs from the calibrated panel — the failures no transcript-reader can see: ASR errors, pronunciation, tone. Each row links to the exact second in the recording."
          stats={[["1,863", "issue rows"], ["1,093", "ASR findings"], ["196", "pronunciation"], ["541", "appropriateness"]]}
          schema="call_id · ts · issue_type · verdict · audio_said · transcripted · reviewer_agreement · recording_url#t="
          note="Every row carries the panel's agreement context — you know how much to trust each finding."
        />
        <DatasetCard
          title="Calibration & reliability"
          color={PURPLE}
          purpose="The meta-dataset: per-reviewer agreement by batch, hidden ground-truth checks, and the anchoring experiment results. This is what makes every other dataset trustworthy — and it ships with them."
          stats={[["78%", "panel agreement ±1"], ["247", "GT calls"], ["25", "reviewers tracked"], ["2", "bias experiments run"]]}
          schema="reviewer_code · batch · agreement_pm1 · gt_agreement · tier · experiment_flags"
          note="Most vendors can't produce this table. It's the reason the other three datasets mean something."
        />
        <div style={{ fontSize: 11, color: MUT }}>
          Counts are live floors from the realloop platform · exports delivered as CSV/JSONL with a data dictionary · PII-scrubbed by default.
        </div>
      </div>
    </PortalShell>
  );
}
